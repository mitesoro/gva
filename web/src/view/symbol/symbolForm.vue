<template>
  <div>
    <div class="gva-form-box">
      <el-form :model="formData" ref="elFormRef" label-position="right" :rules="rule" label-width="80px">
        <el-form-item label="名称:" prop="name">
          <el-input v-model="formData.name" :clearable="true" placeholder="请输入" />
       </el-form-item>
        <el-form-item label="代码:" prop="code">
          <el-input v-model="formData.code" :clearable="true" placeholder="请输入" />
       </el-form-item>
        <el-form-item label="倍数:" prop="multiple">
          <el-input v-model.number="formData.multiple" :clearable="true" placeholder="请输入" />
       </el-form-item>
        <el-form-item label="保证金:" prop="bond">
          <el-input v-model.number="formData.bond" :clearable="true" placeholder="请输入" />
       </el-form-item>
        <el-form-item label="止赢点位:" prop="point_success">
          <el-input v-model.number="formData.point_success" :clearable="true" placeholder="请输入" />
        </el-form-item>
        <el-form-item label="止赢价格:" prop="point_success_price">
          <el-input v-model.number="formData.point_success_price" :clearable="true" placeholder="请输入" />
        </el-form-item>
        <el-form-item label="止损点位:" prop="point_fail">
          <el-input v-model.number="formData.point_fail" :clearable="true" placeholder="请输入" />
        </el-form-item>
        <el-form-item label="止损价格:" prop="point_fail_price">
          <el-input v-model.number="formData.point_fail_price" :clearable="true" placeholder="请输入" />
        </el-form-item>
        <el-form-item label="状态:" prop="status">
          <el-switch v-model="formData.status" active-color="#13ce66" inactive-color="#ff4949" active-text="是" inactive-text="否" clearable ></el-switch>
       </el-form-item>
        <el-form-item label="开始时间:"  prop="start_at" >
          <el-time-select v-model="formData.start_at" :picker-options="{ start: '07:30', step: '00:15', end: '20:30' }" placeholder="选择时间"></el-time-select>
        </el-form-item>
        <el-form-item label="结束时间:"  prop="end_at" >
          <el-time-select v-model="formData.end_at" :picker-options="{ start: '07:30', step: '00:15', end: '20:30' }" placeholder="选择时间"></el-time-select>
        </el-form-item>
        <el-form-item label="特殊时间:"  prop="days" >
          <el-input type="textarea" v-model="formData.days" rows="3"></el-input>
        </el-form-item>
        <el-form-item>
          <el-button type="primary" @click="save">保存</el-button>
          <el-button type="primary" @click="back">返回</el-button>
        </el-form-item>
      </el-form>
    </div>
  </div>
</template>

<script setup>
import {
  createSymbol,
  updateSymbol,
  findSymbol
} from '@/api/symbol'

defineOptions({
    name: 'SymbolForm'
})

// 自动获取字典
import { getDictFunc } from '@/utils/format'
import { useRoute, useRouter } from "vue-router"
import { ElMessage } from 'element-plus'
import { ref, reactive } from 'vue'

const route = useRoute()
const router = useRouter()

const type = ref('')
const formData = ref({
            name: '',
            code: '',
            multiple: 0,
            bond: 0,
            status: false,
            start_at: '',
            end_at: '',
            days: '',
        })
// 验证规则
const rule = reactive({
               name : [{
                   required: true,
                   message: '',
                   trigger: ['input','blur'],
               }],
               code : [{
                   required: true,
                   message: '',
                   trigger: ['input','blur'],
               }],
               multiple : [{
                   required: true,
                   message: '',
                   trigger: ['input','blur'],
               }],
               bond : [{
                   required: true,
                   message: '',
                   trigger: ['input','blur'],
               }],
               status : [{
                   required: true,
                   message: '',
                   trigger: ['input','blur'],
               }],
})

const elFormRef = ref()

// 初始化方法
const init = async () => {
 // 建议通过url传参获取目标数据ID 调用 find方法进行查询数据操作 从而决定本页面是create还是update 以下为id作为url参数示例
    if (route.query.id) {
      const res = await findSymbol({ ID: route.query.id })
      if (res.code === 0) {
        formData.value = res.data.resb
        type.value = 'update'
      }
    } else {
      type.value = 'create'
    }
}

init()
// 保存按钮
const save = async() => {
      elFormRef.value?.validate( async (valid) => {
         if (!valid) return
            let res
           switch (type.value) {
             case 'create':
               res = await createSymbol(formData.value)
               break
             case 'update':
               res = await updateSymbol(formData.value)
               break
             default:
               res = await createSymbol(formData.value)
               break
           }
           if (res.code === 0) {
             ElMessage({
               type: 'success',
               message: '创建/更改成功'
             })
           }
       })
}

// 返回按钮
const back = () => {
    router.go(-1)
}

</script>

